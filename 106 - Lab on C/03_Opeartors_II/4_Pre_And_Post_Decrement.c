#include <stdio.h>

int main() {

    int a = 5;

    printf("a = %d\n", a);
    printf("--a = %d\n", --a);
    printf("a-- = %d\n", a--);

    printf("a = %d && --a = %d && a-- = %d", a, --a, a--);

    return 0;
}
