#include <stdio.h>
#include <limits.h>

int main() {

    // Datatype : int
    printf("\nMinimum limit of int : %d", INT_MIN);
    printf("\nMaximum limit of int : %d", INT_MAX);

    // Datatype : char
    printf("\n\nMinimum limit of char : %d", CHAR_MIN);
    printf("\nMaximum limit of char : %d", CHAR_MAX);

    // Datatype : short
    printf("\n\nMinimum limit of short : %d", SHRT_MIN);
    printf("\nMaximum limit of short : %d", SHRT_MAX);

    // Datatype : long
    printf("\n\nMinimum limit of long : %ld", LONG_MIN);
    printf("\nMaximum limit of long : %ld", LONG_MAX);

    // Datatype : usigned short & unsigned int
    printf("\n\nMaximum limit of unsigned short : %d", USHRT_MAX);
    printf("\nMaximum limit of unsigned long : %lu", UINT_MAX);

}
