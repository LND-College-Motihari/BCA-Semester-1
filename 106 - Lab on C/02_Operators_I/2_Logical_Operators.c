#include <stdio.h>

int main() {
    int a = 0;
    int b = 50;

    // && : Logical AND
    printf("\na && b = %d", a&&b);

    // || : Logical OR
    printf("\na || b = %d", a||b);

    // ! : Logical NOT
    printf("\n!a = %d", !a);
    printf("\n!b = %d", !b);

    return 0;
}