#include <stdio.h>

int main() {
    int a = 100;
    int b = 25;

    // + : Addition
    printf("Sum of a & b = %d\n", a+b);

    // - : Subtraction
    printf("Difference of a & b = %d\n", a-b);
    
    // * : Multiplication
    printf("Product of a & b = %d\n", a*b);
    
    // / : Divison
    printf("Quoitent of a & b = %d\n", a/b);
    
    // % : Remainder
    printf("Remainder of a & b = %d\n", a%b);

    return 0;
}